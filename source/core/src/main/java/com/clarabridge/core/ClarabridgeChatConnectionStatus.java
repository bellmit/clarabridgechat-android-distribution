package com.clarabridge.core;

/**
 * ClarabridgeChat connection status.
 */
public enum ClarabridgeChatConnectionStatus {

    /**
     * User is connected to ClarabridgeChat.
     */
    CONNECTED,

    /**
     * User is disconnected from ClarabridgeChat.
     */
    DISCONNECTED,

    /**
     * User connection with ClarabridgeChat not yet initiated.
     */
    NOT_YET_INITIATED,

    ;
}
